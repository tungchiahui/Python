#古典问题：有一对兔子，从出生后第3个月起每个月都生一对兔子，小兔子长到第三个月后每个月又生一对兔子，假如兔子都不死，问每个月的兔子总数为多少？


month = int(input("请输入月份： "))
if month == 1 or month == 2:    #兔子从第三个月才开始生兔子，所以前两个月单拿出来
    print("总共有兔子数为1只")
else:
    a = 1
    b = 1
    c = 0
    for i in range(3, month + 1):
        c = a + b     #第n个月等于第n-1个月+第n-2个月
        a = b
        b = c
    print("总共有兔子数为：", c)